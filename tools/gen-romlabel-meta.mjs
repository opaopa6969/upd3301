// gen-romlabel-meta — run z80anal over the real ROM dumps and record, per
// labeled routine, the machine-verifiable facts: clobbered/input/saved
// registers, I/O ports, memory touches, T-state range. The prose in
// romlabels.js is human understanding; this file generates the numbers.
//
// usage: node tools/gen-romlabel-meta.mjs   (needs BYO ROMs in roms/)
// output: romlabels-meta.js (committed — it is OUR analysis, not the ROM)
import { readFile, writeFile } from 'node:fs/promises';
import { analyze } from '../z80anal.js';
import { ROM_LABELS } from '../romlabels.js';

const load = async (p) => new Uint8Array(await readFile(new URL(`../${p}`, import.meta.url)));

const cleanIo = (io) => io.map((x) => ({ dir: x.dir, port: x.port, name: x.name ?? null }));
const cleanMem = (mem) => mem.map((x) => ({ dir: x.dir, addr: x.addr, name: x.name ?? null }));

// analyze one address space and pull out the routines we labeled.
// analyze() already serializes: destroys is transitively propagated
// through CALLs and excludes push/pop-saved registers.
function harvest(bytes, org, labels, ports) {
  const symbols = {};
  for (const l of labels) symbols[l.name] = l.addr;
  const { routines } = analyze(bytes, org, symbols, { ports });
  const out = {};
  for (const r of routines) {
    const label = labels.find((l) => l.addr === r.addr);
    if (!label) continue;
    out[label.addr] = {
      clobbers: r.destroys,
      inputs: r.inputs,
      saves: r.saves,
      io: cleanIo(r.io),
      mem: cleanMem(r.mem),
      tStates: r.tStates,
      warnings: r.warnings.length,
      unknown: r.unknown, // indirect flow somewhere → clobbers may be incomplete
      sweptTo: r.end,
    };
  }
  return out;
}

const meta = {};

// N88 main ROM: 32KB at 0000-7FFF. The two bank0 labels live in n88_0.rom
// mapped at 6000-7FFF — a different address space, analyzed separately.
try {
  const n88 = await load('roms/8801mkIIFR/n88.rom');
  const set = ROM_LABELS['n88-fr'].labels;
  const bank0Addrs = new Set([0x754f, 0x75a1]);
  const mainLabels = set.filter((l) => !bank0Addrs.has(l.addr));
  meta['n88-fr'] = harvest(n88, 0x0000, mainLabels, 'pc88-main');

  const ext0 = await load('roms/8801mkIIFR/n88_0.rom');
  const bank0Labels = set.filter((l) => bank0Addrs.has(l.addr));
  Object.assign(meta['n88-fr'], harvest(ext0, 0x6000, bank0Labels, 'pc88-main'));
} catch (e) {
  console.error('n88 skipped:', e.message);
}

try {
  const sub = await load('roms/8801mkIIFR/disk.rom');
  meta.pc80s31 = harvest(sub, 0x0000, ROM_LABELS.pc80s31.labels, 'pc88-sub');
} catch (e) {
  console.error('pc80s31 skipped:', e.message);
}

const body = `// GENERATED by tools/gen-romlabel-meta.mjs — do not edit by hand.
//
// Machine-verified facts per labeled ROM routine, produced by running
// z80anal over the actual dumps. Notes on reading it honestly:
// - the analysis is a LINEAR SWEEP from each label to the next one
//   ('sweptTo'), so a routine ending early in that span over-approximates
// - 'clobbers' = written and not push/pop-protected; propagation through
//   CALLs is NOT applied here (see z80anal for the transitive version)
// - this file contains our analysis of the ROMs, not the ROMs

export const SCHEMA_VERSION = 1;

export const ROM_LABEL_META = ${JSON.stringify(meta, null, 1)};
`;
await writeFile(new URL('../romlabels-meta.js', import.meta.url), body);
const n = Object.values(meta).reduce((a, m) => a + Object.keys(m).length, 0);
console.log(`romlabels-meta.js written: ${n} routines across ${Object.keys(meta).length} sets`);
